class RedHeadDuck implements Duck, Swimmable, Flyable, Quackable {
   
    public void swim() {
        System.out.println("RedHead Duck is swimming.");
    }

    public void fly() {
        System.out.println("RedHead Duck is flying.");
    }

    public void quack() {
        System.out.println("RedHead Duck quacks!");
    }
}